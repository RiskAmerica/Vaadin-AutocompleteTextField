package eu.maxschuster.vaadin.autocompletetextfield;

import java.io.Serializable;
import java.util.Collection;

/**
 * Functional interface that finds {@link AutocompleteSuggestion}s by a given
 * {@link AutocompleteQuery}.
 *
 * @author Max Schuster
 * @see AutocompleteSuggestion
 * @see AutocompleteQuery
 * @see AutocompleteTextFieldExtension
 */
public interface AutocompleteSuggestionProvider extends Serializable {

    /**
     * Finds {@link AutocompleteSuggestion}s for the given
     * {@link AutocompleteQuery}.
     * <p>
     * The implementation should check if
     * {@link AutocompleteQuery#hasLimit()} is {@code true} and limit its
     * suggestions according to the limit provided by
     * {@link AutocompleteQuery#getLimit()}<br>
     * <strong>Excess suggestions will be ignored by the
     * {@link AutocompleteTextFieldExtension}!</strong>
     * </p>
     *
     * @param query The {@link AutocompleteQuery} generated by a
     *              {@link AutocompleteTextFieldExtension}.
     * @return A {@link Collection} of {@link AutocompleteSuggestion}s for the
     * given {@link AutocompleteQuery}
     * @see AutocompleteQuery#hasLimit()
     * @see AutocompleteQuery#getLimit()
     */
    public Collection<AutocompleteSuggestion> querySuggestions(AutocompleteQuery query);

}
